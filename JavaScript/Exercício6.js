// Declare duas variáveis booleanas.
// Utilize os operadores lógicos AND, OR e NOT para realizar
// operações entre essas variáveis e imprima os resultados no console.

let valorA = true;
let valorB = false;

let resultadoAND = valorA && valorB;
let resultadoOR = valorA || valorB;
let resultadoNOT = !valorB;

console.log(resultadoAND);
console.log(resultadoOR);
